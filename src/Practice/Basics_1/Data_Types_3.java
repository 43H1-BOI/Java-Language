package Practice.Basics_1;

public class Data_Types_3 {
    public static void main(String[] args){
        //     --> 1 Byte = 8 Bits

        // Primitive Data Types

        //     --> Integral
        byte anyByte=127; // 1 byte
        short anyShort=6787; // 2 bytes
        int anyInt=3712; // 4 bytes
        long anyLong=378498686; // 8 bytes
        // L can be used for long type value


        //     --> Decimal
        float anyFloat=692.3f; // Not Confirmed
        // Must Include f after decimal numbers for float data type

        double anyDouble=73428.435; //  8 bytes
        // Any Decimal will Become double by Default
        // Can use D or d after Number (Optional)


        //     --> Character
        char anyChar='A'; // 2 bytes
        // Can Store Unicode Characters (Superclass of ASCII Standard)


        //     --> Boolean
        boolean bool=true; // Size Depends on JVM
        // Can Store true or false


        // Literals means value after = sign
        // 2^4 2^3 2^2 2^1 2^0
        // 16   8   4   2   1

    }
}
